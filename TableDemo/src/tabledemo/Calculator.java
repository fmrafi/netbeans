    private void btn_cosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cosActionPerformed
        String btn_Text = btn_cos.getText();
        r(btn_Text);
        total2 = Math.toRadians(total1);
        if (total1 == 90 || total1 == 270 || total1 == 450 || total1 == 630 || total1 == 810) {
            txtDisplay.setText(String.format("0.0"));
        } 
        else if (total1 == -90 || total1 == -270 || total1 == -450 || total1 == -630 || total1 == -810) {
            txtDisplay.setText(String.format("0.0"));
        }
        else {
            txtDisplay.setText(Double.toString(Math.cos(total2)));
        }
        total1 = 0;
    }//GEN-LAST:event_btn_cosActionPerformed

